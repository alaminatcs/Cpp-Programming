#include <bits/stdc++.h>
using namespace std;
int main() {
    int n;
    cin >> n;
    switch(n) {
        case 0:
            cout << "No is: Zero\n";
            break;
        case 1:
            cout << "No is: One\n";
            break;
        case 2:
            cout << "No is: Two\n";
            break;
        case 3:
            cout << "No is: Three\n";
            break;
        case 4:
            cout << "No is: Four\n";
            break;
        default:
            cout << "No is: greater than OR equal to 5\n";
    }
    return 0;
}